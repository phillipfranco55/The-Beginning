

# Gives the area of a circle.
pi = 3.1415
r = input("Enter Radius: ")
radius = float(r)
area = pi * radius ** 2
print("The area of the circle is", area)


# Gives the perimeter of a square.
b = input("Enter the base length of the rectangle: ")
h = input("Enter the height of the rectangle: ")
length = int(b) * 2
height = int(h) * 2
perimeter = length + height
print("The perimeter of the square is", perimeter)
